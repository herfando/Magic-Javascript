// ===== Function with parameter =====
function greet(parameter) {           // 'parameter' is input to function
    return "Hello, " + parameter;    // return greeting string
}

function sum(x, y) {                 // x and y are parameters
    return x + y;                    // return sum of x and y
}

console.log(greet("Herfando"));      // call function with "Herfando"
console.log(sum(5, 10));             // call sum function with 5 and 10
